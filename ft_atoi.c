#include "libft.h"

int	ft_atoi(const char *str)
{
	int		result;
	int		sign;
	size_t	i;
	int		num;

	result = 0;
	sign = 1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = str[i] - '0';
		result = result * 10 + num;
		i++;
	}
	return (result * sign);
}
